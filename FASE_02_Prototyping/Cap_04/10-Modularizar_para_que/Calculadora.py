def somar(a, b):
# Função de somar dois valores
    return a + b

def subtrair(a, b):
# Função para subtrair dois valores
    return a - b

def dividir(a, b):
# Função para dividir dois valores
    if (b != 0):
        return a / b
    return 0

def multiplicar(a,b):
# Função para multiplicar dois valores
    return a * b

